#include <iostream>
using namespace std;

int main() {
      int a,b;

      // Double the integer
      cout << "Enter the number:" << endl;
      cin >> a;
      cout << "The double of your given number is:" << a + a << endl;

      // Sum of two integers
      cout << "Enter first number:" << endl;
      cin >> a;
      cout << "Enter second number:" << endl;
      cin >> b;
      cout << "The Sum of" << a << "and" << b << "is:" << a + b << endl;

      return 0;
}